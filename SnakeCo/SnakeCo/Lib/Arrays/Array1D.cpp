void Cls_Array1D< Type001 >::SetValue( unsigned int i, Type001 Value001 )
{
	//Проверяем значение на выход за пределы
	if( IsInBorders( i, Length ) )
	{
		Array[ i ] = Value001;
	}
}
template < typename Type001 >
void Cls_Array1D< Type001 >::SetValue( unsigned int i, Type001 *Value001 )
{
	//Проверяем значение на выход за пределы
	if( IsInBorders( i, Length ) )
	{
		Array[ i ] = *Value001;
	}
}
