template < typename Type001 >
Type001 Cls_Array1D< Type001 >::GetValue( unsigned int i )
{	
	//Проверяем значение на выход за пределы
	if( IsInBorders( i, Length ) )
	{
		return Array[ i ];
	}
	//Если условие не выполняется,
	//то возвращаем нулевое значение или значения соответствующего типа	
	return 0;
}
