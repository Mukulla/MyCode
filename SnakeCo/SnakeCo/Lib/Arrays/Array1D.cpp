template < typename Type001 >
void Cls_Array1D< Type001 >::SetValues( unsigned int iMin, unsigned int iMax, Type001 Value001 )
{	
	for( unsigned int i = iMin; i < iMax; ++i )
	{
		SetValue( i, Value001 );
	}	
}
template < typename Type001 >
void Cls_Array1D< Type001 >::SetValues( Str_Geminus< unsigned int > Borders001, Type001 Value001 )
{	
	for( unsigned int i = Borders001.Primis; i < Borders001.Secundus; ++i )
	{
		SetValue( i, Value001 );
	}	
}
template < typename Type001 >
void Cls_Array1D< Type001 >::SetValues( Type001 Value001 )
{	
	for( unsigned int i = 0; i < Length; ++i )
	{
		SetValue( i, Value001 );
	}	
}
