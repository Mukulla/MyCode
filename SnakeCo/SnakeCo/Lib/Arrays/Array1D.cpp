template < typename Type001 >
Cls_Array1D< Type001 >::Cls_Array1D()
{
	//Указываем, что массив ещё не создавался
	IsCreated = false;
	//По умолчанию длинна нулевая
	Length = 0;
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////ДЕСТРУКТОР//////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
template < typename Type001 >
Cls_Array1D< Type001 >::~Cls_Array1D()
{
	if( IsCreated )
	{
		delete[] Array;
	}
}
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////СОЗДАТЬ МАССИВ//////////////////////////////////////////////////////////////////////////////////////////////////////////
///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
template < typename Type001 >
void Cls_Array1D< Type001 >::Init( unsigned int Length001 )
{
	//Если массив уже создавался,
	//то удаляем текущие данные и отмечаем, что он не создавался
	if( IsCreated )
	{
		delete[] Array;
		Length = 0;
		IsCreated = false;
	}
	//Защита от нулевой длины
	SetToOne( Length001 );
	//Указываем значение длинны массива
	Length = Length001;
	//Создаём массив
	Array = new Type001[ Length001 ];
	for( unsigned int i = 0; i < Length001; ++i )
	{
		//Инициализируем
		 Array[ i ] = 0;
	}
	//Указываем, что массив был создан
	IsCreated = true;
}
